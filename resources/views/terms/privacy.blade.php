プライバシーポリシー
個人情報の定義
当サイトでは、個人情報について、ユーザー個人に関する情報であって、当該情報を構成する氏名、住所、メールアドレス、生年月日その他の記述等により当該ユーザー個人を識別できる情報とします。

個人情報の適正な取得
当サイトは、利用目的の達成に必要な個人情報を適正に取得し、偽りその他の不正の手段によって取得することはありません。ユーザーは、当サイトが求める全ての情報をご提供いただく必要はありませんが、情報のご提供をいただけない場合、当サイトのサービスの一部が利用できないことがあります。

個人情報の利用目的
当サイトは、個人情報の取得に際して、お知らせした利用目的または以下の目的のために取得した情報を利用します。

なお、ご本人の同意がある場合及び個人情報保護法その他の個人情報に関する法令により認められる場合を除いては、この範囲を超えて個人情報を利用することはありません。

・ 当サイトにおけるユーザーの個人認証
・ 当サイトにおけるユーザーへのサービスの提供
・ 商品広告を提供する企業等への会員サービスを目的とした情報提供
・ 当サイトによるサービスの向上等を目的としたアンケート、キャンペーンの実施
・ 当サイトからのメールによる各種情報の無料提供、お問い合わせへの返答
・ 当サイトによる有料サービス提供時の利用料金請求
・ 利用規約に違反する態様、スパム行為、不正アクセス等の不正利用の防止
・ アクセス状況の把握、ニーズに適したサービスの提供ならびに改善
・ 当サイト及び第三者の広告の配信または表示

個人情報の第三者提供
当サイトは、以下に記載する場合を除き、原則として、ユーザーご本人の同意を得ずに個人情報を第三者に提供しません。

・ 法令に基づく場合
・ 人の生命、身体又は財産の保護のために必要がある場合であって、本人の同意を得ることが困難であるとき
・ 公衆衛生の向上または児童の健全な育成の推進のために特に必要がある場合であって、本人の同意を得ることが困難であるとき
・ 国の機関若しくは地方公共団体又はその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがあるとき
・ 利用目的の達成に必要な範囲内において、個人データの取扱いの全部又は一部を委託する場合
・ 本サービスのサービス向上の目的で個人情報を集計及び分析により得られたものを、個人を識別又は特定できない態様にて提携先等第三者に開示又は提供する場合
・ 商品広告を提供する企業等への会員サービスを目的とした情報提供
・ 広告の配信または表示を目的とした利用



個人情報の安全管理
当サイトは、個人情報の漏えい、減失又は毀損の防止その他の安全管理のための措置を講じるとともに、当サイトの従業員に対する教育を行い、個人情報保護を徹底します。



免責
以下の場合は、第三者による個人情報の取得に関し、当サイトは何らの責任を負いません。

・ ユーザー自らが当サイト上の機能または別の手段を用いて他のユーザーに個人情報を明らかにした場合
・ ユーザー自らが入力した情報またはその他のユーザーが入力した情報により、期せずして本人が特定できてしまった場合
・ ご本人以外のユーザーが個人を識別できる情報（ID、パスワード等）を入手した場合

cookie（クッキー）の使用
当サイトは、よりよいサービスを提供するため、cookie（クッキー）を使用することがありますが、 これにより個人を特定できる情報の収集を行えるものではなく、ユーザーのプライバシーを侵害することはありません。

また、cookie（クッキー）の受け入れを希望しない場合は、ブラウザの設定で変更することができます。

※ cookie（クッキー）とは、サーバーコンピュータからユーザーのブラウザに送信され、ユーザーが使用しているコンピュータのハードディスクに蓄積される情報です。


位置情報の使用
当サイトは、ユーザーが当サイトを利用する際のユーザーの位置情報（緯度経度情報）を、ユーザーの同意がある場合に取得し使用することがあります。

サービス内での行動情報の使用
当サイトは、ユーザーが当サイトを利用されている際の検索条件や、投稿・問合せ内容などお客様がサービス内で行った行動の情報を取得し使用することがあります。

アクセスログ等の記録、開示
当サイトは、当サイトにメッセージ等の書き込みを行ったユーザーのIPアドレス、 当該IPアドレスから当サイトに書き込みを行った日時等のアクセスログを記録することがあります。

当サイトは、ユーザーが開示に同意した場合、犯罪捜査など法律手続の中で開示を要請された場合又は消費者センター、 弁護士会等の公的機関から正当な理由に基づき照会を受けた場合の他、 当サイトになされた書き込み等により損害を被ったと主張する第三者から開示を要請された場合において、 当該書き込みを行ったユーザーの氏名、住所、電子メールアドレス、電話番号などの個人情報及びアクセスログを開示することがあります。

統計データの利用
当サイトは、提供を受けた個人情報をもとに、個人を特定できない形式による統計データを作成し、 当該データにつき何らの制限なく利用することができるものとします。

継続的改善
当サイトは、個人情報の取り扱いに関する運用状況を適宜見直し、継続的な改善に努めるものとし、 必要に応じて、プライバシーポリシーを変更することがあります。

本ポリシーを変更した場合には、本ページ上に掲載いたします。変更された本ポリシーは、掲載を開始した時点から適用されるものとします。



